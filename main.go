package main

import "fmt"

func main() {
    /*
    Go Data Types
    -   bool
    -   Numeric
    -   string
    */

    var a bool = true
    fmt.Println("Boolean: ", a)

    var b int = 5
    fmt.Println("Integer: ", b)

    var c float32 = 3.14
    fmt.Println("Float: ", c)

    var d string = "Hi!"
    fmt.Println("String: ", d)
}
